    <div class="dish">
        <h2>Enchiladas Mineras</h2>
        <p>Las enchiladas mineras son un platillo tradicional de Guanajuato. Consisten en tortillas rellenas de pollo bañadas en una salsa de chile guajillo y decoradas con crema, queso y cebolla.</p>
    </div>

    <div class="dish">
        <h2>Pozole</h2>
        <p>El pozole es una sopa espesa de maíz hominy con carne de cerdo o pollo. Se sirve con rábanos, lechuga, orégano, chile en polvo y limón. Es un platillo muy popular en la región.</p>
    </div>

    <div class="dish">
        <h2>Caldo de Rata</h2>
        <p>Aunque el nombre pueda parecer extraño, el caldo de rata es una deliciosa sopa de chile guajillo con pedacitos de tortilla, aguacate y queso. Es una experiencia culinaria única de Guanajuato.</p>
    </div>

    <div class="dish">
        <h2>Chiles en Nogada</h2>
        <p>Este platillo típico mexicano también tiene su versión guanajuatense. Son chiles poblanos rellenos de picadillo (carne de res y cerdo mezclada con frutas y especias) cubiertos con una salsa de nuez y decorados con granada y perejil.</p>
    </div>

    <div class="dish">
        <h2>Gorditas</h2>
        <p>Las gorditas son pequeñas tortillas rellenas de diferentes guisos como chicharrón prensado, picadillo, frijoles, etc. Se cocinan en comal y se sirven con salsa y crema.</p>
