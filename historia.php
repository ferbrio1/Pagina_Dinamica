    <h1>Historia de Guanajuato</h1>

    <p>Guanajuato tiene una rica historia que se remonta a la época prehispánica. Fue habitado originalmente por grupos chichimecas y otomíes. En el siglo XV, los españoles llegaron a la región y establecieron la ciudad de Guanajuato en 1554.</p>

    <p>La ciudad se convirtió en uno de los centros mineros más importantes de México durante la época colonial, gracias a la abundancia de plata en la zona. Esta prosperidad económica se refleja en la arquitectura barroca de la ciudad y en la construcción de impresionantes edificios y monumentos.</p>

    <p>Durante la Guerra de Independencia de México, Guanajuato fue escenario de importantes eventos. La Alhóndiga de Granaditas, un antiguo almacén de granos, fue tomada por insurgentes y se convirtió en un símbolo de la lucha por la independencia.</p>

    <p>Después de la independencia, Guanajuato siguió desempeñando un papel importante en la historia de México. Hoy en día, la ciudad y el estado de Guanajuato son reconocidos por su patrimonio histórico y cultural, y son destinos turísticos populares para quienes buscan conocer más sobre la historia de México.</p>
